<template>
  <div id="app-header">
    <input type="text" placeholder="请输入你的任务名称，按回车键确认" v-model="inputTodo" @keyup.enter="add"/>
  </div>
</template>

<script>
  export default {
    props:['addTodo'],
    data(){
      return{
        inputTodo:''
      }
    },
    methods:{
      add(){
        const inputTodo=this.inputTodo.trim()
        if(!inputTodo){
          return
        }
        const todo = {
          title:inputTodo,
          checked:false
        }
        this.addTodo(todo)
        this.inputTodo=''
      }
    }
  }
</script>

